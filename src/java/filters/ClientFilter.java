package filters;

/**
 *
 * @author Ivan
 */
public class ClientFilter {
    private String сlientID;
    private String passportSeries;
    private String passportNumber;
    private String firstname;
    private String lastname;
    private String middlename;
    private String telephoneNumber;

    /**
     * @return the сlientID
     */
    public String getСlientID() {
        return сlientID;
    }

    /**
     * @param сlientID the сlientID to set
     */
    public void setСlientID(String сlientID) {
        this.сlientID = сlientID;
    }

    /**
     * @return the passportSeries
     */
    public String getPassportSeries() {
        return passportSeries;
    }

    /**
     * @param passportSeries the passportSeries to set
     */
    public void setPassportSeries(String passportSeries) {
        this.passportSeries = passportSeries;
    }

    /**
     * @return the passportNumber
     */
    public String getPassportNumber() {
        return passportNumber;
    }

    /**
     * @param passportNumber the passportNumber to set
     */
    public void setPassportNumber(String passportNumber) {
        this.passportNumber = passportNumber;
    }

    /**
     * @return the firstname
     */
    public String getFirstname() {
        return firstname;
    }

    /**
     * @param firstname the firstname to set
     */
    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    /**
     * @return the lastname
     */
    public String getLastname() {
        return lastname;
    }

    /**
     * @param lastname the lastname to set
     */
    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    /**
     * @return the middlename
     */
    public String getMiddlename() {
        return middlename;
    }

    /**
     * @param middlename the middlename to set
     */
    public void setMiddlename(String middlename) {
        this.middlename = middlename;
    }

    /**
     * @return the telephoneNumber
     */
    public String getTelephoneNumber() {
        return telephoneNumber;
    }

    /**
     * @param telephoneNumber the telephoneNumber to set
     */
    public void setTelephoneNumber(String telephoneNumber) {
        this.telephoneNumber = telephoneNumber;
    }
}
