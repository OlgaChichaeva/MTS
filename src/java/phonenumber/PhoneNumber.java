package phonenumber;

/**
 *
 * @author Ivan
 */
public class PhoneNumber {
    private int simID;
    private long number;

    /**
     * @return the simID
     */
    public int getSimID() {
        return simID;
    }

    /**
     * @param simID the simID to set
     */
    public void setSimID(int simID) {
        this.simID = simID;
    }

    /**
     * @return the number
     */
    public long getNumber() {
        return number;
    }

    /**
     * @param number the number to set
     */
    public void setNumber(long number) {
        this.number = number;
    }
}
